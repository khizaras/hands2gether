export const getCategories = async () => {
  return new Promise((resolve, reject) => {
    getDocs(collection(db, "categories"))
      .then((querySnapshot) => {
        const categories = [];
        querySnapshot.forEach((doc) => {
          categories.push({
            _id: doc.id,
            ...doc.data(),
          });
        });
        resolve(categories);
      })
      .catch((error) => {
        reject(error);
      });
  });
};

export const updateCategoryApi = async (category) => {
  console.log({ category });
  return new Promise((resolve, reject) => {
    const categoryRef = doc(db, "categories", category._id);
    setDoc(categoryRef, category)
      .then((res) => {
        resolve(res);
      })
      .catch((error) => {
        reject(error);
      });
  });
};

export const createCategoryApi = async (category) => {
  return new Promise((resolve, reject) => {
    const collectionRef = collection(db, "categories");
    const docRef = doc(collectionRef);
    const documentUuid = docRef.id;
    const data = {
      id: documentUuid,
      _id: documentUuid,
      ...category,
      fields: [
        {
          name: "title",
          placeholder: "Enter Title",
          label: "Title",
          type: "text",
          required: true,
        },
        {
          name: "description",
          placeholder: "Enter Description",
          label: "Description",
          type: "textarea",
          required: true,
        },
      ],
    };
    setDoc(docRef, { uuid: documentUuid, ...data })
      .then((res) => {
        resolve(res);
      })
      .catch((error) => {
        reject(error);
      });
  });
};

